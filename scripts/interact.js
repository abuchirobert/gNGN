const contractAddress = "0x5FbDB2315678afecb367f032d93F642f64180aa3";

// Replace with your provider URL (e.g., Infura, Alchemy, or local node)
const providerUrl = "http://127.0.0.1:8545/";

// Replace with your wallet private key
const privateKey = "0xac0974bec39a17e36ba4a6b4d238ff944bacb478cbed5efcae784d7bf4f2ff80";

async function main() {
    // Connect to the provider
    const provider = new ethers.JsonRpcProvider(providerUrl);

    // Create a signer
    const wallet = new ethers.Wallet(privateKey, provider);

    // Create a contract instance
    const contract = new ethers.Contract(contractAddress, abi, wallet);

    // Example address to interact with
    const address = "0x70997970C51812dc3A010C7d01b50e0d17dc79C8";

    // Check balance
    const balance = await contract.getBalance(address);
    console.log(`Balance of ${address}: ${ethers.utils.formatEther(balance)} tokens`);

    // Mint tokens
    const mintAmount = ethers.parseEther("10"); // Mint 10 tokens
    const mintTx = await contract.mint(address, mintAmount);
    await mintTx.wait();
    console.log(`Minted ${ethers.formatEther(mintAmount)} tokens to ${address}`);

    // Burn tokens
    const burnAmount = ethers.parseEther("5"); // Burn 5 tokens
    const burnTx = await contract.burn(address, burnAmount);
    await burnTx.wait();
    console.log(`Burned ${ethers.formatEther(burnAmount)} tokens from ${address}`);
}

main().catch(console.error);
